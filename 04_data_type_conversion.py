num_str = str(666)
print(type(num_str), num_str)

num_float = float(666)
print(type(num_float), num_float)

num_int = int("111")
print(type(num_int), num_int)

num_int2 = int(111.1111)
print(type(num_int2), num_int2)

"""
<class 'str'> 666
<class 'float'> 666.0
<class 'int'> 111
<class 'int'> 111
"""